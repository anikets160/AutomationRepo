package dataTypesAndVariables;

public class ArithmeticOperations {

	public static void main(String[] args) {

		int a = 10;
		int b = 20;
		
		//Addition
		System.out.println(a+b);
			
		//Subtraction
		System.out.println(b-a);
		
		//Multiplication
		System.out.println(a*b);
		
		//Division
		System.out.println(a/b);
				
		//Remainder
		System.out.println(a%b);
	}

}
