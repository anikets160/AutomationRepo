package loops;

public class NestedForLoop {

	public static void main(String[] args) {
		
		// Nested For Loop
		for (int row = 1; row <=4; row++) {
			for (int col = 1; col <=2; col++) {
				
				System.out.print("Row No:=" + row + " Col No:=" + col + " ");
			}
			System.out.println();
		}
		
		
		
	}

}
